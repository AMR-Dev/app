    public boolean verificarProyecto(Proyecto proyecto) { // si el proyecto ya esta asignado a ese empleado da 1
        boolean check = false;
        for(int i = 0;i < JFramePrincipal.sistema.getLocal(indexLocal).getEmpleado(indexEmpleado).getProyecto().size();i++) {
            if(JFramePrincipal.sistema.getLocal(indexLocal).getEmpleado(indexEmpleado).getProyecto(i).equals(proyecto)) {
                check = true;
            }
        }
        return check;
    }
    
