    private void asignarProyectoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_asignarProyectoActionPerformed
        if (JFramePrincipal.sistema.getProyectos().size() < 1) {
            asignarProyecto_text.setForeground(Color.RED);
            asignarProyecto_text.setText("Este proyecto no existe.");
        } else if(verificarProyecto(JFramePrincipal.sistema.getProyectos().get(proyectos.getSelectedIndex()))) {
            asignarProyecto_text.setForeground(Color.RED);
            asignarProyecto_text.setText("Este proyecto ya esta asignado.");
        } else {
            JFramePrincipal.sistema.getLocal(indexLocal).getEmpleado(indexEmpleado).addProyecto(JFramePrincipal.sistema.getProyectos().get(proyectos.getSelectedIndex()));
            JFramePrincipal.sistema.getProyecto(proyectos.getSelectedIndex()).addEmpleado(JFramePrincipal.sistema.getLocal(indexLocal).getEmpleado(indexEmpleado));
            actualizarListaProyectosAsignados();
            asignarProyecto_text.setForeground(Color.BLACK);
            asignarProyecto_text.setText("Proyecto asignado con exito.");
        }
    }//GEN-LAST:event_asignarProyectoActionPerformed

