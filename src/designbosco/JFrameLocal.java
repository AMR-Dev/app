    private void anadirEquipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_anadirEquipoActionPerformed
        if(sistemaOperativoEquipo.getText().isBlank() ||
        plantaEquipo.getText().isBlank() ||
        habitacionEquipo.getText().isBlank() ) {
            equipoDarDeAlta.setForeground(Color.RED);
            equipoDarDeAlta.setText("Faltan campos para rellenar.");
        } else if (!chequearSiEsEntero(plantaEquipo.getText())) {
            equipoDarDeAlta.setForeground(Color.RED);
            equipoDarDeAlta.setText("La planta tiene que ser un entero.");
        } else if (!chequearSiEsEntero(habitacionEquipo.getText())) {
            equipoDarDeAlta.setForeground(Color.RED);
            equipoDarDeAlta.setText("La habitacion tiene que ser un entero.");
        } else {
            JFramePrincipal.sistema.getLocal(index).addEquipo(new Equipo(sistemaOperativoEquipo.getText(), Integer.valueOf(plantaEquipo.getText()), Integer.valueOf(habitacionEquipo.getText())));
            actualizarListaEquipos();
            equipoDarDeAlta.setForeground(Color.BLACK);
            equipoDarDeAlta.setText("Equipo anadido satisfactioriamente.");
        }
    }//GEN-LAST:event_anadirEquipoActionPerformed

