    private void guardarDepartamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarDepartamentoActionPerformed
        if (departamento.getSelectedIndex() == 0) {
            cambios_text.setForeground(Color.RED);
            cambios_text.setText("Falta elegir depatamento.");
        } else {
            JFramePrincipal.sistema.getLocal(index).setDepartamento(departamento.getSelectedIndex());
            cambios_text.setForeground(Color.BLACK);
            cambios_text.setText("Departamento cambiado satisfactoriamente.");
        }
    }//GEN-LAST:event_guardarDepartamentoActionPerformed

    private void guardarCiudadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarCiudadActionPerformed
        if(ciudad.getText().isBlank()) {
            cambios_text.setForeground(Color.RED);
            cambios_text.setText("La ciudad no puede estar en blanco.");
        } else {
            JFramePrincipal.sistema.getLocal(index).setCiudad(ciudad.getText());
            cambios_text.setForeground(Color.BLACK);
            cambios_text.setText("Ciudad cambiada satisfactoriamente.");
        }
    }//GEN-LAST:event_guardarCiudadActionPerformed

